#include "libft.h"

char	*ft_strdup(const char *s)
{
	char*str;
	int i;

	i = 0;
	while (s[i])
		i++;
	if (!(str = malloc(sizeof(*str) * i + 1)))
		return (0);
	i = 0;
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
