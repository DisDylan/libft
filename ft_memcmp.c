#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t i;

	i = 0;
	if (n == 0)
		return (0);
	while (i < n)
	{
		if (((unsigned char*)s2)[i] == ((unsigned char*)s1)[n - 1])
			return (0);
		i++;
	}
	return (-1);
}
